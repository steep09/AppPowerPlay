import UIKit

extension UIButton {
    func setSeletedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4197305441, green: 0.2098149061, blue: 0.2073803544, alpha: 1)
    }
    func setDeselectColor() {
        self.backgroundColor = #colorLiteral(red: 0.4197305441, green: 0.2098149061, blue: 0.2073803544, alpha: 0.5)
    }
}
